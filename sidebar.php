    <h2>A Common Ground</h2>
    <p>The disciplines of Mathematics and Music share an intertwined history stretching back more than two and a half 
millennia. More recently, informatics has made possible new approaches to these disciplines, often with transformative 
effect. International MCM conferences, held biennially, continue to promote the collaboration and exchange of ideas among 
researchers in music theory, mathematics, computer science, musicology, cognition, and other related fields, to 
further our understanding of a wide range of musical phenomena.</p>
    
